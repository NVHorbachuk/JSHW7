// 1) Відкриваємо нове вікно розміром 300x300
let myWindow = window.open("", "", "width=300,height=300");

// 2) Через 2 секунди змінюємо розмір вікна на 500x500
setTimeout(() => {
  myWindow.resizeTo(500, 500);
}, 2000);

// 3) Ще через 2 секунди переміщаємо вікно в точку (200, 200)
setTimeout(() => {
  myWindow.moveTo(200, 200);
}, 4000);

// 4) Ще через 2 секунди закриваємо вікно
setTimeout(() => {
  myWindow.close();
}, 6000);

